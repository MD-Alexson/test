    function videoSize(){
        var block = $(".faq_videos .faq_video").first();
        var w = block.outerWidth();
        var h = Math.round(w / 1.777777);
        $(".faq_videos .faq_video").each(function(){
            $(this).children('a').css({'height': h+'px', 'line-height': h+'px'});
            $(this).css({'height': h+'px'});
        });
    }
    $(document).ready(function () {
        $(".faq_block").children('a').on('click', function(){
            var faq = $(this).parent();
            var div = $(this).siblings("div");
            if(faq.hasClass('active')){
                faq.removeClass('active');
                div.slideUp();
            } else {
                faq.addClass('active');
                div.slideDown();
            }
        });
        $(".faq_videos .faq_video a").on('click', function(e){
            e.preventDefault();
            var video_id = $(this).data('id');
            $.fancybox("#faq_video", {
                afterShow: function(){
                    $("#faq_video").html('<iframe src="https://www.youtube.com/embed/'+video_id+'?autoplay=1" frameborder="0" allowfullscreen></iframe>');
                },
                afterClose: function(){
                    $("#faq_video").html("");
                }
            });
        });
    });
    $(window).load(function(){
        videoSize();
    });
    $(window).on('resize', function(){
        videoSize();
    });
