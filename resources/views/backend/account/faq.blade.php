                <h2>Работа с пользователями, клиентами:</h2>
                <div class='faq_block'>
                    <a href='javascript: void(0);'>Как добавить, импортировать или экспортировать моих пользователей?</a>
                    <div>
Добавление пользователей или клиентов в ваш проект происходит несколькими способами:<br/><br/>
<strong>1. Ручное добавление по-одному.</strong><br/>
Для добавления пользователей нажмите на вкладку "Пользователи" слева в меню, выберите пункт "Список пользователей". В открывшейся странице нажмите кнопку "Добавить пользователей". В странице, которая открылась, введите обязательные данные пользователя, такие как имя, email, пароль, а также дополнительные данные при необходимости: телефон, уровень доступа, ограничение аккаунта пользователя до заданной даты, статус пользователя. Нажмите кнопку "Сохранить".<br/><br/>

<strong>2. Импорт и экспорт.</strong><br/>
Для импорта пользователей вручную нажмите на вкладку "Пользователи" слева в меню, выберите пункт "Импорт".<br/>
Вы можете импортировать пользователей вручную, указав email, имя и телефон через запятую с каждой новой строки в поле ввода. Обязательным является только email.<br/>
Например:<br/>
<pre>
test@gmail.com, Имя Фамилия, +998901111111
test2@gmail.com, Имя Фамилия 2
test3@gmail.com.
</pre>
<br/><br/>
Для импорта пользователей из CSV таблицы нажмите на вкладку "Импортировать таблицу CSV".<br/>
Записи CSV файла импорта обязаны содержать минимум одно обязательное уникальное поле email.<br/>
Необязательные поля:<br/>
- name (Имя);<br/>
- phone (Номер телефона);<br/>
- password (Пароль);<br/>
- level_name (Название уровня доступа, существующего на выбранном проекте);<br/>
- expire (Ограничивать ли доступ пользователя до даты: 1 - да, 0 - нет);<br/>
- expires (Дата, до которой будет ограничен доступ пользователю по Гринвичу (UTC-0));<br/>
- status (Статус пользователя: 1 - активен, 0 - деактивирован)<br/>
<br/><br/>
Скачайте образец таблицы CSV, нажав на кнопку “Скачать образец”. Наполните скачанную таблицу данными пользователей, сохраните файл и импортируйте его в ABC Кабинет, нажав кнопку "Загрузить файл". Введите дополнительные настройки на странице и нажмите кнопку "Импортировать" внизу.<br/><br/>
Если вы верно создали данные ваших пользователей в таблице по заданному образцу, они появятся в виде таблицы в разделе “Список пользователей”, во вкладке “Пользователи”.<br/><br/>
Для экспорта пользователей в таблицу CSV нажмите на вкладку "Пользователи" слева в меню, выберите пункт "Экспорт". В открывшейся странице нажмите кнопку “Экспорт пользователей проекта в CSV файл”.<br/><br/>
<strong>3. Автоматическое добавление.</strong><br/>
Автоматическое добавление пользователей в ваш проект будет происходить после того, как вы настроите оплату продуктов во вкладке "Настройки оплаты" слева в меню, и после того, как пользователи будут оплачивать ваши продукты в E-autopay или Justclick, в зависимости от того, какую систему вы используете и настроите.<br/>
